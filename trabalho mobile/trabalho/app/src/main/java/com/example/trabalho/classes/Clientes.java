package com.example.trabalho.classes;

public class Clientes {
    private String nome;
    private String cpf;

    public Clientes() {}

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
}
